#include "3-calc.h"

/**
* op_add - operation addition
* @a: parameter a
* @b: parameter b
* Return: return result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - operation substuction
* @a: parameter a
* @b: parameter b
* Return: return result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - operation multiplication
* @a: parameter a
* @b: parameter b
* Return: return result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_dev - operation devision
* @a: parameter a
* @b: parameter b
* Return: return result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - operation Model
* @a: parameter a
* @b: parameter b
* Return: return result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
